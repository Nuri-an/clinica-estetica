    private void fileSelectorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fileSelectorActionPerformed
        // TODO add your handling code here:
        File file = fileSelector.getSelectedFile();
        System.err.println(evt.getActionCommand());

        if (evt.getActionCommand().equals("CancelSelection")) {
            fileSelector.setVisible(false);
            panelTable.setVisible(true);
            return;
        }

        String nameFile = file.getName();
        System.out.println(file);

        int response = JOptionPane.showConfirmDialog(null,
                "Deseja adicionar o contrato "
                + nameFile + " ?",
                "Confirmar o upload",
                JOptionPane.OK_CANCEL_OPTION,
                JOptionPane.QUESTION_MESSAGE);

        if (response == JOptionPane.OK_OPTION) {
            File destino = new File("src/main/resources/contracts/" + nameFile);
            try {
                this.attendance.saveFile(file, destino);
            } catch (IOException ex) {
                Logger.getLogger(FrAppWithoutContract.class.getName()).log(Level.SEVERE, null, ex);
            }

            try {
                appointmentCon.onSendContract(this.attendance.getAppointment().getId(), nameFile);
                this.appointmentCon.onFindAllWithoutContract(table, user.getPeople().getCPF(), fieldSearch.getText());
                fileSelector.setVisible(false);
                panelTable.setVisible(true);
                JOptionPane.showMessageDialog(this, "Contrato adicionado com sucesso!");
            } catch (AppointmentException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }

    }//GEN-LAST:event_fileSelectorActionPerformed

